#Tuplas e Dicionarios
#Tuplas usa-se []

print(tuple([0,1,2]))  #3 itens

print(tuple(['abc', 1,3]))

# Dicionario é um objeto de mapeamento, mutável, que utiliza objetos hashable como chave de mapeamento 
# para objetos arbitrarios e heterogêneos.
#Dicionario usa-se {}

print(['Bolo'],{'Pão'}) #Tupla + dicionario

c = dict(zip(['one','two','three'], [1,2,3]))

print(c)